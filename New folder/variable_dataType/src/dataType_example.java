public class dataType_example {
    public static void main(String[] args) {
        int i = 10;// khai báo biến i là kiểu int có giá trị 10
        float f = 20.5f;// khai báo biến f là kiểu float có giá trị 20.5 chú ý là phải có giá trị 20.5f
        double d = 20.5;//khai báo biến d là kiểu double có giá trị 20.5
        boolean b = true;// khai báo biến b kiểu boolean có giá trị true
        char c = 'a'; // khai báo biến c có kiểu là char có giá trị là a chú ý ký tự chả thì phải là dấu nháy đơn ''
        String s = "Đà Nẵng";// khai báo biến s có kiểu là string có giá trị là chuỗi Đà nẵng
        System.out.println("i = " + i);
        System.out.println("f = " + f);
        System.out.println("d = " + d);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("s = " + s);
    }
}
